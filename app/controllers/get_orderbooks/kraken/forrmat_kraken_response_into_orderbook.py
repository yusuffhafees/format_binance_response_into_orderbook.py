def format_kraken_response_into_orderbook(example, instrument):
    my_data = example['result']
    x = my_data.get(list(example['result'])[0])
    #print(x)
    ask = x.get("asks")
    bid = x.get("bids")

    asks = [Order(instrument=list(example['result'].keys())[0],
                  price=decimal.Decimal(price),
                  quantity=decimal.Decimal(quantity),
                  side='ask',
                  insertion_time=datetime.datetime.fromtimestamp(insertion_time),
                  ) for price, quantity, insertion_time in ask]

    bids = []
    for price, quantity, insertion_time in bid:
        my_order = Order(price=decimal.Decimal(price),
                         quantity=quantity,
                         side='bid',
                         insertion_time=datetime.datetime.fromtimestamp(insertion_time),
                         instrument=list(example['result'].keys())[0])
        bids.append(my_order)

    return OrderBook(instrument=instrument,
                     bids=bids,
                     asks=asks)



